package jp.ac.fjb.safna.bean;


public class ProductBean{
	
	private String code = "";
	private String name = "";
	private String category = "";
	private int genka = 0;
	private int teika = 0;
	private int baika = 0;
	private int kosu = 0;
	private int genzai = 0;
	private int hanbai = 0;

	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}

	public int getGenka() {
		return genka;
	}
	public void setGenka(int genka) {
		this.genka = genka;
	}

	public int getTeika() {
		return teika;
	}
	public void setTeika(int teika) {
		this.teika = teika;
	}

	public int getBaika() {
		return baika;
	}
	public void setBaika(int baika) {
		this.baika = baika;
	}

	public int getKosu() {
		return kosu;
	}
	public void setKosu(int kosu) {
		this.kosu = kosu;
	}
	public int getGenzai() {
		return genzai;
	}
	public void setGenzai(int genzai) {
		this.genzai = genzai;
	}
	public int getHanbai() {
		return hanbai;
	}
	public void setHanbai(int hanbai) {
		this.hanbai = hanbai;
	}



}
